package model;

public class Arrangement {

	private int number;
	
	private int line;
	
	public Arrangement(int num,int lin) {
		this.number=num;
		this.line=lin;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public int getLine() {
		return line;
	}

	public void setLine(int line) {
		this.line = line;
	}
	
	
	
}
